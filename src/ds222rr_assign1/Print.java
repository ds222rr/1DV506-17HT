package ds222rr_assign1;

public class Print {

	public static void main(String[] args) {
		// on one line
		System.out.println("Knowledge is power!\n");
		// on three lines, one word on each line
		System.out.print("Knowledge\nis\npower!\n\n");
		// inside a rectangle made up by the characters = and |
		System.out.print("=====================\n"
						+"|Knowledge is power!|\n"
						+"=====================");
	}

}
